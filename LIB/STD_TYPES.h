#ifndef _STD_TYPES_H
#define _STD_TYPES_H

#define NULL ((void*)0)

typedef unsigned char u8;
typedef unsigned short int u16;
typedef unsigned long int u32;

typedef signed char s8;
typedef signed short int s16;
typedef signed long int s32;

typedef float f32;
typedef double f64;

// configuration of constant types

typedef const unsigned char ku8;
typedef const unsigned short int ku16;
typedef const unsigned long int ku32;

typedef const signed char ks8;
typedef const signed short int ks16;
typedef const signed long int ks32;

typedef const float kf32;
typedef const double kf64;

#endif
